
; SOUND
; AX = FREQ (HZ)

SOUND:		PUSH	BX
		PUSH	DX
		MOV	BX,AX
		MOV	DX,0X0012
		MOV	AX,0X34DC
		DIV	BX
		MOV	BL,AL
		MOV	AL,0XB6
		OUT	0X43,AL
		MOV	AL,BL
		OUT	0X42,AL
		MOV	AL,AH
		OUT	0X42,AL
		IN	AL,0X61
		OR	AL,0X03
		OUT	0X61,AL
		POP	DX
		POP	BX
		RET

NOSOUND:	IN	AL,0X61
		AND	AL,0XFC
		OUT	0X61,AL
		RET
